    {
        // request contÃ© el $_POST[input1,input2,input3...] 
        $objClub = new Club();
        $objClub->name = $request->get('inpNom');
        $objClub->palmares = $request->get('inpPal');
        $objClub->foundation_year_month = $request->get('inpFun');

        $objClub->save();
        return redirect('/clubs');
    }

